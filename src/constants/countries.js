/**
 * 국가 코드로 한국어 국가명 조회
 * @param {string} countryCode - ISO 3166-1 alpha-2 국가 코드
 * @returns {string} 한국어 국가명 (없으면 국가 코드 반환)
 */
export function getCountryName(countryCode) {
  return COUNTRIES[countryCode?.toUpperCase()] || countryCode || '알 수 없음'
}

/**
 * 모든 국가 목록을 배열로 반환 (검색용)
 * @returns {Array<{code: string, name: string}>} 국가 목록
 */
export function getAllCountries() {
  return Object.entries(COUNTRIES).map(([code, name]) => ({
    code,
    name,
  }))
}

/**
 * 국가명으로 국가 검색
 * @param {string} query - 검색어
 * @returns {Array<{code: string, name: string}>} 검색 결과
 */
export function searchCountries(query) {
  if (!query || query.trim() === '') {
    return getAllCountries()
  }
  
  const lowerQuery = query.toLowerCase()
  return getAllCountries().filter(
    (country) =>
      country.name.toLowerCase().includes(lowerQuery) ||
      country.code.toLowerCase().includes(lowerQuery)
  )
}

